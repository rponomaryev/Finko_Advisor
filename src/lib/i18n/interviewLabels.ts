export function translateOptionValue(locale: AppLocale, value: unknown): string {
  if (value === null || value === undefined || value === "") return labelValue(value, locale);
  const text = String(value);
  const key = text
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .toLowerCase();
  return optionLabels[locale]?.[key] ?? optionLabels[locale]?.[text] ?? labelValue(value, locale);
}

