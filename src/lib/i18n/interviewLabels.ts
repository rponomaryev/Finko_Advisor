function questionDomainText(question: InterviewQuestion): string {
  return [
    question.blockId,
    question.semanticGroup,
    question.label,
    question.question,
    ...(question.capabilityTags ?? [])
  ].filter(Boolean).join(" ").toLowerCase();
}

function shouldUseAdaptiveQuestionCopy(question: InterviewQuestion): boolean {
  if (!contextSensitiveAdaptiveQuestionKeys.has(question.key)) return true;
  const domain = questionDomainText(question);

  if (/car_wash|автомой|мойк[аи]? авто|автохим|автомобил|автопарк/.test(domain)) return true;
  if (/auto_service|автосервис|запчаст|подъемник|подъ[её]мник/.test(domain)) return true;
  if (/tool_rental|аренда инструмента|инструмент/.test(domain)) return true;
  if (/laundry|прачеч|бель[ея]|стирк|сушильн/.test(domain)) return false;
  if (/cleaning_service|клининг|уборк/.test(domain)) return false;
  if (/solar_installation|солнеч|панел|электр/.test(domain)) return false;
  if (/device_repair|смартфон|ноутбук|ремонт устройств/.test(domain)) return false;
  return false;
}

export function translateQuestion(locale: AppLocale, question: InterviewQuestion): InterviewQuestion {
  const sanitize = (value: string | undefined) => value ? replaceForbiddenUserFacingTerms(value, locale) : value;
  const localizedCopy = (question as InterviewQuestion & { localizedCopy?: Partial<Record<AppLocale, QuestionCopy & { helpText?: string }>> }).localizedCopy?.[locale];
  if (localizedCopy?.label || localizedCopy?.question || localizedCopy?.placeholder || localizedCopy?.unit || localizedCopy?.helpText) {
    return {
      ...question,
      label: sanitize(localizedCopy.label ?? question.label) ?? question.label,
      question: sanitize(localizedCopy.question ?? question.question) ?? question.question,
      placeholder: sanitize(localizedCopy.placeholder ?? question.placeholder),
      helpText: sanitize(localizedCopy.helpText ?? question.helpText),
      unit: sanitize(localizedCopy.unit ?? question.unit ?? undefined)
    };
  }
  const adaptiveCopy = shouldUseAdaptiveQuestionCopy(question) ? adaptiveQuestions[locale]?.[question.key] : undefined;
  const copy = { ...(questions[locale]?.[question.key] ?? {}), ...(extraQuestions[locale]?.[question.key] ?? {}), ...(adaptiveCopy ?? {}) } as QuestionCopy;
  if (!copy.label && !copy.question && !copy.placeholder && !copy.unit) {
    if (locale === "ru") return { ...question, label: sanitize(question.label) ?? question.label, question: sanitize(question.question) ?? question.question, helpText: sanitize(question.helpText) };
    const label = question.key.split(".").pop()?.split("_").filter(Boolean).map((part) => part.charAt(0).toUpperCase() + part.slice(1)).join(" ") ?? question.label;
    return {
      ...question,
      label: sanitize(label) ?? label,
      question: locale === "uz" ? `${sanitize(label) ?? label} bo'yicha ma'lumot kiriting.` : `Provide details for ${label}.`
    };
  }
  return {
    ...question,
    label: sanitize(copy.label ?? question.label) ?? question.label,
    question: sanitize(copy.question ?? question.question) ?? question.question,
    placeholder: sanitize(copy.placeholder ?? question.placeholder),
    helpText: sanitize(question.helpText),
    unit: sanitize(copy.unit ?? question.unit ?? undefined)
  };
}

