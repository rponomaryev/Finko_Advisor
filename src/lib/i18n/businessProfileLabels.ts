function humanizeProfileValue(value: string, locale: AppLocale): string {
  const normalized = normalizeProfileKey(value);
  if (!/^[a-z0-9_]+$/.test(normalized) || !normalized.includes("_")) return value;
  const sampleLabel = businessSampleProfileLabels[normalized]?.[locale];
  if (sampleLabel && !isGenericProfileOutputLabel(sampleLabel)) return sampleLabel;
  const exact = exactTechnicalProfileLabels[normalized]?.[locale];
  if (exact) return exact;
  const text = normalized.replace(/_/g, " ");
  if (locale === "en") return text.replace(/\b\w/g, (char) => char.toUpperCase());
  const dictionary = profileTokenLabels[locale];
  const words = normalized.split("_").filter(Boolean).map((token) => dictionary[token] ?? token);
  const phrase = words.join(" ").replace(/\s+/g, " ").trim();
  if (!phrase) return locale === "uz" ? "Aniqlashtirilmagan ko'rsatkich" : "Уточняемый показатель";
  return phrase.charAt(0).toUpperCase() + phrase.slice(1);
}

function isRawTechnicalValue(value: string): boolean {
  const normalized = normalizeProfileKey(value);
  return /^[a-z0-9_]+$/.test(normalized) && (
    normalized.includes("_")
    || value === normalized
    || /^[a-z]+[A-Z][A-Za-z0-9]*$/.test(value)
  );
}

function fallbackProfileValue(value: string, locale: AppLocale): string {
  const raw = String(value ?? "").trim();
  if (!raw || raw === "—") return raw || "—";
  if (!isRawTechnicalValue(raw)) return raw;
  return humanizeProfileValue(raw, locale);
}

export function hasBusinessProfileLabel(value: string | null | undefined): boolean {
  const raw = String(value ?? "").trim();
  if (!raw) return true;
  const normalized = normalizeProfileKey(raw);
  return Boolean(businessProfileLabels[normalized] || businessProfileLabels[raw]);
}

export function localizeBusinessProfileValue(
  value: string | string[] | null | undefined,
  locale: AppLocale,
  options: { failOnUnknown?: boolean } = {}
): string {
  if (Array.isArray(value)) {
    return value.map((item) => localizeBusinessProfileValue(item, locale, options)).join(", ");
  }

  const raw = String(value ?? "").trim();
  if (!raw || raw === "—") return raw || "—";

  const mixed = mixedProfilePhraseLabels[normalizeMixedProfilePhrase(raw)]?.[locale];
  if (mixed) return mixed;

  if (raw.includes(",") && !businessProfileLabels[raw]) {
    return raw
      .split(",")
      .map((part) => localizeBusinessProfileValue(part.trim(), locale, options))
      .join(", ");
  }

  const normalized = legacyProfileKey(raw) ?? normalizeProfileKey(raw);
  const sampleDirect = businessSampleProfileLabels[normalized]?.[locale] ?? businessSampleProfileLabels[raw]?.[locale];
  const direct = businessProfileLabels[normalized]?.[locale] ?? businessProfileLabels[raw]?.[locale];
  if (direct && !isGenericProfileOutputLabel(direct)) return direct;
  if (sampleDirect && !isGenericProfileOutputLabel(sampleDirect)) return sampleDirect;
  if (direct && isGenericProfileOutputLabel(direct)) return fallbackProfileValue(raw, locale);

  if (options.failOnUnknown && process.env.NODE_ENV !== "production" && isRawTechnicalValue(raw)) {
    throw new Error(`Missing business profile localization for technical value: ${raw}`);
  }

  return fallbackProfileValue(raw, locale);
}

export function localizeProfileValue(
  value: string | string[] | null | undefined,
  locale: AppLocale
): string {
  return localizeBusinessProfileValue(value, locale, { failOnUnknown: false });
}
