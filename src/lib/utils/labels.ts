function normalizeEnumLookupKey(text: string): string {
  return text
    .trim()
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/[\s-]+/g, "_")
    .toLowerCase()
    .replace(/^_+|_+$/g, "");
}

const enumMissingWarnings = new Set<string>();

function warnMissingEnumTranslation(text: string, locale: AppLocale): void {
  if (process.env.NODE_ENV === "production") return;
  if (!/^[a-z][a-z0-9_]*$/i.test(text) || !text.includes("_")) return;
  const message = `[i18n] Missing enum translation for ${text} (${locale}); using mechanical fallback.`;
  if (process.env.NODE_ENV === "test") {
    throw new Error(message);
  }
  const key = `${locale}:${text}`;
  if (enumMissingWarnings.has(key)) return;
  enumMissingWarnings.add(key);
  console.warn(message);
}

export function getCanonicalEnumLabel(value: string, locale: AppLocale): string | undefined {
  const raw = String(value ?? "").trim();
  if (!raw) return undefined;
  const normalized = normalizeEnumLookupKey(raw);
  const candidates = Array.from(new Set([raw, normalized]));
  for (const key of candidates) {
    const direct = localizedValueLabels[locale][key]
      ?? exactEnumFallbackLabels[locale][key]
      ?? businessProfileLabels[key]?.[locale]
      ?? businessSampleProfileLabels[key]?.[locale];
    if (direct) return direct;
  }
  return undefined;
}

export function hasCanonicalEnumLabel(value: string): boolean {
  return (["ru", "uz", "en"] as const).every((locale) => Boolean(getCanonicalEnumLabel(value, locale)));
}

export function missingCanonicalEnumLocales(value: string): AppLocale[] {
  return (["ru", "uz", "en"] as const).filter((locale) => !getCanonicalEnumLabel(value, locale));
}

function humanizeEnumValue(text: string, locale: AppLocale) {
  const canonical = getCanonicalEnumLabel(text, locale);
  if (canonical) return canonical;
  if (!/^[a-z]+(_[a-z0-9]+)+$/i.test(text)) return text;
  warnMissingEnumTranslation(text, locale);
  const tokenLabels = enumTokenLabels[locale];
  const words = text.split("_").map((token) => tokenLabels[token] ?? token);
  const label = words.join(" ");
  return locale === "en" ? label.replace(/\b\w/g, (char) => char.toUpperCase()) : label.charAt(0).toUpperCase() + label.slice(1);
}

function formatMoneyLikeLabel(value: Record<string, unknown>, locale: AppLocale): string | null {
  const amount = Number(value.amountUZS ?? value.sourceAmount ?? value.amount);
  const currency = String(value.sourceCurrency ?? value.currency ?? "UZS");
  if (!Number.isFinite(amount)) return null;
  const formatted = Math.round(amount).toLocaleString(locale === "en" ? "en-US" : locale === "uz" ? "uz-UZ" : "ru-RU").replace(/\u00A0/g, " ");
  if (locale === "en") return `${currency} ${formatted}`;
  if (locale === "uz") return currency === "UZS" ? `${formatted} so‘m` : `${formatted} ${currency}`;
  return currency === "UZS" ? `${formatted} сум` : `${formatted} ${currency}`;
}

export function labelValue(value: unknown, locale: AppLocale = "ru"): string {
  const labels = localizedValueLabels[locale];
  if (value && typeof value === "object" && !Array.isArray(value)) {
    const moneyLabel = formatMoneyLikeLabel(value as Record<string, unknown>, locale);
    if (moneyLabel) return moneyLabel;
    const values = Object.values(value as Record<string, unknown>)
      .map((item) => labelValue(item, locale))
      .filter((item) => item && !/^(не указано|not specified|ko'rsatilmagan)$/i.test(item));
    return values.length ? Array.from(new Set(values)).join(", ") : locale === "ru" ? "Не указано" : locale === "uz" ? "Ko'rsatilmagan" : "Not specified";
  }
  if (Array.isArray(value)) {
    return value.length ? value.map((item) => labelValue(item, locale)).join(", ") : locale === "ru" ? "Не указано" : locale === "uz" ? "Ko'rsatilmagan" : "Not specified";
  }
  if (typeof value === "boolean") {
    return value ? labels.yes : labels.no;
  }
  if (value === null || value === undefined || value === "") {
    return locale === "ru" ? "Не указано" : locale === "uz" ? "Ko'rsatilmagan" : "Not specified";
  }
  const text = String(value);
  return getCanonicalEnumLabel(text, locale) ?? humanizeEnumValue(text, locale);
}

